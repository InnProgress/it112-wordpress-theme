<script>
  const carousels = document.querySelectorAll(".carousel-wrapper");
  carousels.forEach(carouselWrapper => {
    const carouselPrevButton = carouselWrapper.querySelector(".carousel__prev");
    const carouselNextButton = carouselWrapper.querySelector(".carousel__next");

    let currentCarouselItem = 0;

    const carousel = carouselWrapper.querySelector(".carousel");

    const carouselItems = carouselWrapper.querySelectorAll(".carousel__item");
  
    carouselPrevButton.addEventListener("click", () => {
      if (currentCarouselItem <= 0) currentCarouselItem = carouselItems.length - 1;
      else currentCarouselItem--;
      updateCarousel();
    });

    carouselNextButton.addEventListener("click", () => {
      if (currentCarouselItem >= carouselItems.length - 1) currentCarouselItem = 0;
      else currentCarouselItem++;
      updateCarousel();
    });

    const updateCarousel = () => {
      carousel.scrollTo({
        top: currentCarouselItem * carousel.clientHeight,
        left: 0,
        behavior: "smooth",
      });
    };
  });
</script>
